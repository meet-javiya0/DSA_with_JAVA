public class Product_of_a_and_b_6_4 {
    public static int product(int a, int b) {
        int product = a * b;
        return product;
    }

    public static void main(String args[]) {
        int a = 3;
        int b = 5;
        int product = product(a, b);
        System.out.println("Product is:" + product);
    }
}