public class Relational_Operations_2_3 {
    public static void main(String args[]) {
        int a = 10;
        int b = 5;
        System.out.println((a == b)); // Equal to operator
        System.out.println((a != b)); // Not Equal to operator
        System.out.println((a > b)); // Greater than operator
        System.out.println((a < b)); // Less then operator
        System.out.println((a >= b)); // Greater or Equal to operator
        System.out.println((a <= b)); // Less or Equal to operator
    }
}