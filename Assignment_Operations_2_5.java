public class Assignment_Operations_2_5 {
    public static void main(String args[]) {
        int a = 10; // Equal to operator
        System.out.println(a);
        a += 5; // Plus equal to operator
        System.out.println(a);
        a -= 5; // Minus equal to operator
        System.out.println(a);
        a *= 5; // Multiply equal to operator
        System.out.println(a);
        a /= 5; // Divide equal to operator
        System.out.println(a);
    }
}